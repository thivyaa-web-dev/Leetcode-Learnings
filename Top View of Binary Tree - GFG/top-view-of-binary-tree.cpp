class Solution
{
    public:
    //Function to return a list of nodes visible from the top view 
    //from left to right in Binary Tree.
    vector<int> topView(Node *root)
    {
        //Your code here
        //this is where we store the topview
        vector<int>ans;
        //if the tree is empty return the ans
        if(root==NULL) return ans;
        //you can create your own map and queue 
        map<int,int>mpp;
        queue<pair<Node*,int>>q;
        //now you enter the root and the line initially as 0
        q.push({root,0});
        //after that you keep on traversing while the q is not empty 
        while(!q.empty()){
            //get the topmost node
            auto it = q.front();
            q.pop();
            //get the line node
            Node*node = it.first;
            int line = it.second;
            //make sure that the line exists on the map 
            //you check if the line exists
            //once the line exists you initialize the node on the map
            if(mpp.find(line) == mpp.end()) mpp[line] = node->data;
            
            if(node->left!=NULL){
                q.push({node->left,line-1});
            }
            if(node->right!=NULL){
                q.push({node->right,line+1});
            }
        }
        //once you have done this, iterate on the map
        for(auto it:mpp){
            //make sure, you push it into your ans vector 
            ans.push_back(it.second);
        }
        return ans;
    }

};



